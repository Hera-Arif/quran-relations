function AyeNo2Sura(ayeno){
  for(var i in SuraInfo) {
    var tmp = ayeno - SuraInfo[i][0]
    if (0 < tmp && tmp <= SuraInfo[i][1])
    {
      return i;
    }
  }
}

function AyeSura2Ruku(ayesura) {
  var sura = 1*ayesura.replace(/:.*/, '')
  var aye = 1*ayesura.replace(/.*:/, '')
  for(var i in Ruku) {
    if (!Ruku[i][0]) continue;
    if (Ruku[i][0] < sura) continue;
    if (Ruku[i][0] > sura) return i-1;
    if (Ruku[i][1] < aye) continue;
    if (Ruku[i][1] > aye) return i-1;
    return i
  }
}

function AyeNo2Name(ayeno, useno){
  for(var i in SuraInfo) {
    var tmp = ayeno - SuraInfo[i][0]
    if (0 < tmp && tmp <= SuraInfo[i][1])
    {
      if (useno)
        return i+':'+tmp
      else
        return SuraNames[i]+':'+tmp
    }
  }
}

function AyeSuraNo2Name(ayesura){
  a = ayesura.split(':')
  return SuraNames[1*a[0]] + ':' + a[1]
}

function FindAyeNo(sura, aye) {
  if(aye == null) {
    var tmp = sura.split(':')
    sura = 1*tmp[0]
    aye = 1*tmp[1]
  }
  return SuraInfo[sura][0]+aye;
}

